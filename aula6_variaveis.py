# Variáveis são usadas para salvar algo na memória do computador
# PEP8: inicie váriáveis com letras minúsculas e se for nome composto, separe com underline
# O sinal de igual (=) é o operador de atribuição. Ele é usado para atribuir um valor a um nome (variável)
# Uso: nome_variavel = expressão

nome_completo = 'Angelina Jolie'
print(nome_completo) # Imprime o valor da variável nome_completo

soma_dois_mais_dois = 2 + 2
print(soma_dois_mais_dois) # Imprime o valor da variável soma_dois_mais_dois

print(nome_completo, soma_dois_mais_dois) # Imprime os valores das variáveis nome_completo e soma_dois_mais_dois

nome = 'Angelina'
