# Tipo de dado bool - booleano (lógico)
# Só aceita dois valores: True (sim) ou False (não) (sempre com a inicial maiúscula)
#Existem vários operadores para "questionar". Dentre eles o == operador lógico que questiona se um valor é igual ao outro.
print(10 == 10)  # Sim -> True (verdadeiro)
print(10 == 20)  # Não -> False (falso)
print(10 + 10 == 20)  # Sim -> True (verdadeiro)
print(type(10 == 10))  # bool
