# Tipos int e float 
# int - números inteiros
# O tipo de int representa qualquer número inteiro, positivo ou negativo, sem parte decimal. Em Python, o tipo int pode representar números de tamanho arbitrário, limitado apenas pela memória disponível no sistema.
# Positivo e negativo. Int sem sinal é considerado positivo.
print(1234)  # int positivo
print(-1234)  # int negativo
print(0)  # int zero

# float - números reais (com parte decimal)
print(1.234)  # float positivo
print(-1.234)  # float negativo
print(0.0)  # float zero

# A função type mostra o tipo do dado em Python
print(type(1234))  # int
print(type('Fernanda'))  # str
print(type(1.234))  # float
