# strings são textos em Python. Sempre entre aspas simples ou duplas.
print('Olá, Mundo!')  # aspas simples

# Escape
print('Olá, \'Mundo\'!')  # aspas simples dentro de aspas simples

# r
print(r'Olá, \'Mundo\'!')  # raw string - quando queremos que apareça o caractere de escape

# Escape mais limpo
print('Fernanda "Guimarães"')  # aspas duplas dentro de aspas simples
print("Fernanda 'Guimarães'")  # aspas simples dentro de aspas duplas